package javarexecicios;

public class calculadora {
    public static void soma(double numero1, double numero2){
        double resultadosoma=numero1+numero2;
        System.out.println("resultado da soma é:"+ resultadosoma);
    }
    public static void subtracao(double numero1, double numero2){
        double resultadosub=numero1-numero2;
        System.out.println("resultado da subtração é:"+ resultadosub);
    }
    public static void mult(double numero1, double numero2){
        double resultadomult=numero1*numero2;
        System.out.println("resultado da multiplicação é:"+ resultadomult);
    }
    public static void divisao(double numero1, double numero2){
        double resultadodiv=numero1/numero2;
        System.out.println("resultado da divisão é:"+ resultadodiv);
    }
}
